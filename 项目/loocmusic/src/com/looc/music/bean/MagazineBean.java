public class MagazineBean {
	private String styleName;
	private Integer magazineID;
	private String title;
	private String magazineImg;
	private String text;
	private String musicID;
	private String name;
	private String musicImg;
	private String src;
	public String getStyleName() {
		return styleName;
	}
	public void setStyleName(String styleName) {
		this.styleName = styleName;
	}
	public Integer getMagazineID() {
		return magazineID;
	}
	public void setMagazineID(Integer magazineID) {
		this.magazineID = magazineID;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getMagazineImg() {
		return magazineImg;
	}
	public void setMagazineImg(String magazineImg) {
		this.magazineImg = magazineImg;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getMusicID() {
		return musicID;
	}
	public void setMusicID(String musicID) {
		this.musicID = musicID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMusicImg() {
		return musicImg;
	}
	public void setMusicImg(String musicImg) {
		this.musicImg = musicImg;
	}
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}

	
	
}
